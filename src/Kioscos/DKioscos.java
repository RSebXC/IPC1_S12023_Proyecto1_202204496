package Kioscos;

/**
 *
 * @author rodri
 */
public class DKioscos {
    String CodigoK;
    String Nombre;
    String CodigoR;

    public DKioscos(String CodigoK, String Nombre, String CodigoR) {
        this.CodigoK = CodigoK;
        this.Nombre = Nombre;
        this.CodigoR = CodigoR;
    }

    public String getCodigoK() {
        return CodigoK;
    }

    public void setCodigoK(String CodigoK) {
        this.CodigoK = CodigoK;
    }

    public String getNombre() {
        return Nombre;
    }

    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }

    public String getCodigoR() {
        return CodigoR;
    }

    public void setCodigoR(String CodigoR) {
        this.CodigoR = CodigoR;
    }


    
    
}
